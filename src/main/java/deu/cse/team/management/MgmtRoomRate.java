    private void MgmtRateSearchRoomBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_MgmtRateSearchRoomBtnActionPerformed
        // TODO add your handling code here:
        String str = MgmtRateRoomNumField.getText();
        for (int i = 0; i < MgmtRoomRateTable.getRowCount(); i++) {
            if (str.equals(MgmtRoomRateTable.getValueAt(i, 0))) {
                MgmtRoomRateTable.requestFocus();
                MgmtRoomRateTable.changeSelection(i, 0, false, false);
            }
        }
    }//GEN-LAST:event_MgmtRateSearchRoomBtnActionPerformed

    private void ModifyRoomBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ModifyRoomBtnActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) MgmtRoomRateTable.getModel();
        int row = MgmtRoomRateTable.getSelectedRow();
        try {
            ModifyRoomNumLabel.setText(model.getValueAt(row, 0).toString());
            ModifyRoomRateField.setText(model.getValueAt(row, 1).toString());
            if (model.getValueAt(row, 2).toString().equals("Y"))
                RoomStatusYRB.setSelected(true);
            else
                RoomStatusNRB.setSelected(true);
            ModifyRoomDlg.setVisible(true);
            ModifyRoomDlg.setLocationRelativeTo(this);
            ModifyRoomDlg.setSize(400, 400);
        } catch (ArrayIndexOutOfBoundsException ex) {
            JOptionPane.showMessageDialog(null, "선택된 항목이 없습니다.");
        }
    }//GEN-LAST:event_ModifyRoomBtnActionPerformed

