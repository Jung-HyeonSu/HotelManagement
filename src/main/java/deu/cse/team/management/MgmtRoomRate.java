    private void ModifyRoomDataOkBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ModifyRoomDataOkBtnActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) MgmtRoomRateTable.getModel();
        int index = Integer.parseInt(ModifyRoomNumLabel.getText());
        String status;
        if (RoomStatusYRB.isSelected())
            status = "Y";
        else
            status = "N";
            
        model.removeRow(index);
        model.insertRow(index, new Object[] {
            ModifyRoomNumLabel.getText(),
            ModifyRoomRateField.getText(),
            status
        });
        try {
            new ModifyRoomData(model);
            JOptionPane.showMessageDialog(null, "수정 완료");
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "오류 발생!");
        }
        ModifyRoomDlg.dispose();
    }//GEN-LAST:event_ModifyRoomDataOkBtnActionPerformed

