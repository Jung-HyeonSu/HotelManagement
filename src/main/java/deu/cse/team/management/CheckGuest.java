    private void SearchPeriodBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchPeriodBtnActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) CheckGuestTable.getModel();
        model.setRowCount(0);
        String entranceDate = EntranceDateCB1.getSelectedItem().toString()
                + EntranceDateCB2.getSelectedItem().toString()
                + EntranceDateCB3.getSelectedItem().toString();
        String exitDate = ExitDateCB1.getSelectedItem().toString()
                + ExitDateCB2.getSelectedItem().toString()
                + ExitDateCB3.getSelectedItem().toString();
        if (Integer.parseInt(entranceDate) <= Integer.parseInt(exitDate)) {
            LoadBookingData loadBookingData = new LoadBookingData();
            loadBookingData.loadData(model, Integer.parseInt(entranceDate), Integer.parseInt(exitDate));
        } else {
            JOptionPane.showMessageDialog(null, "날짜가 유효하지 않습니다.");
        }
    }//GEN-LAST:event_SearchPeriodBtnActionPerformed

