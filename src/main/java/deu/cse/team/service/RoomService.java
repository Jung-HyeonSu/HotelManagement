    private void ConfirmRoomServiceActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ConfirmRoomServiceActionPerformed
        // TODO add your handling code here:
        FileMgmt fileMgmt = new FileMgmt();
        String index = OrderIndexField.getText();
        String service = "룸서비스";
        String room = RoomNumCB.getSelectedItem().toString();
        String time = new CurrentTime().getCurrentTime();
        String productname = null;
        String money = null;
        for (int i = 0; i < jTable2.getRowCount(); i++) {
            if (i == 0) {
                productname = String.valueOf(jTable2.getValueAt(i, 0));
                money = String.valueOf(jTable2.getValueAt(i, 1));
            } else {
                productname = productname.concat("/" + String.valueOf(jTable2.getValueAt(i, 0)));
                money = money.concat("/" + String.valueOf(jTable2.getValueAt(i, 1)));
            }
        }

        String str = index + "\t" + service + "\t" + room + "\t" + time + "\t" + productname + "\t" + money;
        try {
            fileMgmt.writeServiceOrderListFileData("C:\\DB\\ServiceOrderList.txt", str);
        } catch (IOException ex) {
            Logger.getLogger(RoomService.class.getName()).log(Level.SEVERE, null, ex);
        }
        JOptionPane.showMessageDialog(null, "주문이 완료되었습니다.");
        initServiceList();
    }//GEN-LAST:event_ConfirmRoomServiceActionPerformed

    private void OnsitePaymentBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_OnsitePaymentBtnActionPerformed
        // TODO add your handling code here:
        int row = jTable2.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(null, "현장 결제할 상품을 선택해주세요.");
        } else {
            try {
                jTable2.setValueAt(0, row, 1);
            } catch (Exception e) {
            }
        }
    }//GEN-LAST:event_OnsitePaymentBtnActionPerformed

